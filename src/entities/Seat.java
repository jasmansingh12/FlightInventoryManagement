package entities;

public class Seat {
    String seatNumber;
    String fareType;

    public String getSeatNumber() {
        return seatNumber;
    }

    public void setSeatNumber(String seatNumber) {
        this.seatNumber = seatNumber;
    }

    public String getFairType() {
        return fareType;
    }

    public void setFairType(String fairType) {
        this.fareType = fairType;
    }
}
